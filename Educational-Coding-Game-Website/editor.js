    async handleImageFile(file) {
        if (!file.type.startsWith('image/')) {
            this.showMessage('Please upload a valid image file', 'error');
            return;
        }

        const maxSizeBytes = 5 * 1024 * 1024;
        if (file.size > maxSizeBytes) {
            this.showMessage('Image is too large. Maximum size is 5MB.', 'error');
            return;
        }

        try {
            this.setImageUploadStatus('Processing image...', 'info');
            const originalDataUrl = await this.readFileAsDataUrl(file);
            const originalBytes = this.estimateDataUrlBytes(originalDataUrl);

            let finalDataUrl = originalDataUrl;
            let finalBytes = originalBytes;
            let compressionApplied = false;

            // Keep GIF as-is to avoid losing animation frames.
            if (file.type !== 'image/gif') {
                const compressed = await this.compressImageDataUrl(originalDataUrl, {
                    maxWidth: 1600,
                    maxHeight: 1600,
                    quality: 0.82
                });
                const compressedBytes = this.estimateDataUrlBytes(compressed);

                if (compressedBytes < originalBytes * 0.98) {
                    finalDataUrl = compressed;
                    finalBytes = compressedBytes;
                    compressionApplied = true;
                }
            }

            this.uploadedImageDataUrl = finalDataUrl;
            this.uploadedImageMeta = { name: file.name, size: finalBytes, type: 'image/data-url' };
            this.previewImage(this.uploadedImageDataUrl);

            if (compressionApplied) {
                const reduction = Math.round((1 - (finalBytes / originalBytes)) * 100);
                this.setImageUploadStatus(
                    `Uploaded ${file.name}: ${this.formatFileSize(originalBytes)} -> ${this.formatFileSize(finalBytes)} (${reduction}% smaller)`,
                    'success'
                );
            } else {
                this.setImageUploadStatus(`Uploaded ${file.name}: ${this.formatFileSize(finalBytes)}`, 'success');
            }
        } catch (error) {
            console.error('Image upload/compression error:', error);
            this.showMessage('Failed to process image file', 'error');
            this.setImageUploadStatus('Failed to process image file', 'error');
        }
    }

    formatFileSize(bytes) {
        if (bytes < 1024) {
            return `${bytes} B`;
        }
        if (bytes < 1024 * 1024) {
            return `${(bytes / 1024).toFixed(1)} KB`;
        }
        return `${(bytes / (1024 * 1024)).toFixed(2)} MB`;
    }

    estimateDataUrlBytes(dataUrl) {
        if (!dataUrl || typeof dataUrl !== 'string') {
            return 0;
        }
        const commaIndex = dataUrl.indexOf(',');
        if (commaIndex === -1) {
            return 0;
        }
        const base64 = dataUrl.slice(commaIndex + 1);
        const padding = (base64.match(/=+$/) || [''])[0].length;
        return Math.floor((base64.length * 3) / 4) - padding;
    }

    readFileAsDataUrl(file) {
        return new Promise((resolve, reject) => {
            const reader = new FileReader();
            reader.onload = () => resolve(reader.result);
            reader.onerror = () => reject(new Error('Failed to read file'));
            reader.readAsDataURL(file);
        });
    }

    loadImageElementFromDataUrl(dataUrl) {
        return new Promise((resolve, reject) => {
            const image = new Image();
            image.onload = () => resolve(image);
            image.onerror = () => reject(new Error('Failed to load image'));
            image.src = dataUrl;
        });
    }

    async compressImageDataUrl(dataUrl, options = {}) {
        const image = await this.loadImageElementFromDataUrl(dataUrl);
        const maxWidth = options.maxWidth || 1600;
        const maxHeight = options.maxHeight || 1600;
        const quality = options.quality || 0.82;

        const widthScale = maxWidth / image.width;
        const heightScale = maxHeight / image.height;
        const scale = Math.min(1, widthScale, heightScale);

        const targetWidth = Math.max(1, Math.round(image.width * scale));
        const targetHeight = Math.max(1, Math.round(image.height * scale));

        const canvas = document.createElement('canvas');
        canvas.width = targetWidth;
        canvas.height = targetHeight;

        const ctx = canvas.getContext('2d');
        ctx.imageSmoothingEnabled = true;
        ctx.imageSmoothingQuality = 'high';
        ctx.drawImage(image, 0, 0, targetWidth, targetHeight);

        // Prefer WEBP for better compression; fallback to JPEG when unavailable.
        let compressedDataUrl = canvas.toDataURL('image/webp', quality);
        if (!compressedDataUrl.startsWith('data:image/webp')) {
            compressedDataUrl = canvas.toDataURL('image/jpeg', quality);
        }

        return compressedDataUrl;
    }


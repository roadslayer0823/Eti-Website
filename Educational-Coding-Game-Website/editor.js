    async saveLessonToServer(lessonData) {
        const token = localStorage.getItem('eti_jwt_token');
        if (!token) {
            return false;
        }

        try {
            const response = await fetch(`${CODING_API_BASE}/api/lesson-content`, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'Authorization': `Bearer ${token}`
                },
                body: JSON.stringify({ lesson: lessonData })
            });

            if (!response.ok) {
                console.warn('Server save failed:', response.status, await response.text());
                return false;
            }
            return true;
        } catch (error) {
            console.warn('Server save error:', error);
            return false;
        }
    }

    updateStagesIndex(lessonData) {
        const indexData = JSON.parse(localStorage.getItem('stagesIndex') || '{"stages": []}');
        
        const existingStage = indexData.stages.find(s => s.id === lessonData.id);
        if (existingStage) {
            existingStage.title = lessonData.title;
            existingStage.description = lessonData.description;
            existingStage.lastUpdated = new Date().toISOString();
        } else {
            indexData.stages.push({
                id: lessonData.id,
                title: lessonData.title,
                description: lessonData.description,
                createdAt: lessonData.createdAt
            });
        }
        
        indexData.lastUpdated = new Date().toISOString();
        localStorage.setItem('stagesIndex', JSON.stringify(indexData));
    }

